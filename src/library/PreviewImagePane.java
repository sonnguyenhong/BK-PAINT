    public PreviewImagePane() {
        initComponents();
        this.setSize(new Dimension(2*ImageCell.WIDTH, 2*ImageCell.HEIGHT));
    }
    
    public void showPreviewImage(BufferedImage selectedImage){
        this.selectedImage = selectedImage;
        repaint();
    }
    
    public void paintComponent(Graphics g){
        super.paintChildren(g);
        if(selectedImage == null){
            selectedImage = (BufferedImage) this.createImage(2*ImageCell.WIDTH, 2*ImageCell.HEIGHT);
            g2 = (Graphics2D) selectedImage.createGraphics();
            
            g2.drawImage(selectedImage, 0, 0, 2*ImageCell.WIDTH, 2*ImageCell.HEIGHT, null);
        }
        g.drawImage(selectedImage, 0, 0, 2*ImageCell.WIDTH, 2*ImageCell.HEIGHT, null);
    }

